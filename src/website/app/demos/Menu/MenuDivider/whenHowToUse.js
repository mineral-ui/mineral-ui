export default `MenuDividers are used to visually separate
[MenuGroups](/components/menu-group) or individual
[MenuItems](/components/menu-item) to establish hierarchy in your Menu. They
could be used to separate items in a long list if all elements exist on the same
conceptual level. For example, if your Menu is composed of a long list of songs,
a MenuDivider could be placed between songs that start with A, B, C, etc.

Do not use MenuDividers to simply separate options as a decoration, or to 
provide "underlines" to the MenuItem.`;
